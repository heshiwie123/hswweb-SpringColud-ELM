package com.he.elm.config;

import com.he.elm.common.constant.Constant;
import org.springframework.amqp.core.Binding;
import org.springframework.amqp.core.BindingBuilder;
import org.springframework.amqp.core.Queue;
import org.springframework.amqp.core.TopicExchange;
import org.springframework.amqp.support.converter.Jackson2JsonMessageConverter;
import org.springframework.amqp.support.converter.MessageConverter;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.http.converter.json.MappingJackson2HttpMessageConverter;

@Configuration
public class TopicRabbitConfig {

    //绑定键
    public final static String orders = Constant.ORDERS;
    public final static String email = Constant.EMAIL;

    @Bean
    public Queue firstQueue() {
        return new Queue(TopicRabbitConfig.orders);
    }
    @Bean
    public Queue secondQueue() {
        return new Queue(TopicRabbitConfig.email);
    }

    @Bean
    TopicExchange exchange() {
        return new TopicExchange(Constant.TOPIC_EXCHANGE);
    }

    //将firstQueue和topicExchange绑定,而且绑定的键值为topic.man
    //这样只要是消息携带的路由键是topic.man,才会分发到该队列
    @Bean
    Binding bindingExchangeMessage() {
        return BindingBuilder.bind(firstQueue()).to(exchange()).with(orders);
    }

    //将secondQueue和topicExchange绑定,而且绑定的键值为用上通配路由键规则topic.#
    // 这样只要是消息携带的路由键是以topic.开头,都会分发到该队列
    @Bean
    Binding bindingExchangeMessage2() {
        return BindingBuilder.bind(secondQueue()).to(exchange()).with("topic.#");
    }

}
